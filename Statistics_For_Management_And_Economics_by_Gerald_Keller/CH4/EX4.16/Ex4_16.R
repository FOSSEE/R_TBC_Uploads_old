# Calculating the Coefficient of Correlation 

#Set 1
x1 <- c(2,6,7)
y1 <- c(13,20,27)
cor(x1,y1)
#Answer: Correlation coefficient for Set 1: 0.9449112

#Set 2
x2 <- c(2,6,7)
y2 <- c(27,20,13)
cor(x2,y2)
#Answer: Correlation coefficient for Set 2: -0.9449112

#Set 3
x3 <- c(2,6,7)
y3 <- c(20,27,13)
cor(x3,y3)
#Answer: Correlation coefficient for Set 3: -0.1889822

