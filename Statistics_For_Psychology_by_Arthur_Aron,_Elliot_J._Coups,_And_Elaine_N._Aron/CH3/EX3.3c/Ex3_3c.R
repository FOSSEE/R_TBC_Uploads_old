# Finding a Probability

apple <- 20
strawberry <- 20
cherry <- 5
grape <- 5

total <- apple + strawberry + cherry + grape

case <- cherry + grape   

probability <- case / total

cat("Probability of picking one piece of candy at random which will be either cherry or grape 
    is", probability)