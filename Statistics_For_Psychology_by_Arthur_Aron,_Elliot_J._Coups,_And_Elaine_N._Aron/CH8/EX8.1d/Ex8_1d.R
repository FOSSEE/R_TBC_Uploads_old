# The t score for the difference between the two actual means

m1 <- 198   # Mean of the first sample
m2 <- 190   # Mean of the second sample

diff_sd <- 3.04   # Standard deviation difference

t <- (m1 - m2) / diff_sd   # t score

cat("t score is", round(t, 2))