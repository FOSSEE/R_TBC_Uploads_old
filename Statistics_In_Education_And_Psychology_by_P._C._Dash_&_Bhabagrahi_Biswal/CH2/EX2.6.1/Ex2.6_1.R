#(a) class limit
lower_limit<-10
upper_limit<-20

lower_value<-lower_limit-0.5
upper_value<-upper_limit+0.5

cat("The lower and upper values of the class interval 10-20 is",lower_value,upper_value)

#(b) class midpoint

midpoint<-lower_limit+(upper_limit-lower_limit)/2

cat("mid point of a class interval 10-20 is", midpoint)
