#If each score is added or subtracted by a constant quantity
#The mean will increased by same quantity 
X<-c(1,2,3,4,5,6,7)
mean(X)

# Addition of constant 5
X_add<-X+5
mean(X_add)
mean(X)+5 # the same constant addition

# subtrction of constant 5
X_sub<-X-5
mean(X_sub)
mean(X)-5 # the same constant subtraction
            